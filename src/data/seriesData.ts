function generateEpisodes(
  seasonNum: number,
  count: number,
  seriesSlug: string,
  baseDuration: number = 42
): Episode[] {
  return Array.from({ length: count }, (_, i) => ({
    id: `${seriesSlug}-s${seasonNum}-e${i + 1}`,
    number: i + 1,
    title: `الحلقة ${i + 1}`,
    duration: `${baseDuration + Math.floor(Math.random() * 10)} دقيقة`,
    thumbnail: `https://picsum.photos/seed/${seriesSlug}-s${seasonNum}-e${i + 1}/400/225`,
    videoUrl: `https://example.com/videos/${seriesSlug}/s${seasonNum}/e${i + 1}.mp4`,
    description: `الحلقة ${i + 1} من الموسم ${seasonNum} - أحداث مشوقة ومثيرة`,
    airDate: `2024-${String(Math.min(12, seasonNum * 2 + 1)).padStart(2, '0')}-${String(Math.min(28, (i % 28) + 1)).padStart(2, '0')}`,
  }));
}

function makeSeasons(seriesSlug: string, seasonCounts: number[], baseDuration?: number) {
  return seasonCounts.map((count, idx) => ({
    id: `${seriesSlug}-season-${idx + 1}`,
    number: idx + 1,
    title: `الموسم ${idx + 1}`,
    episodes: generateEpisodes(idx + 1, count, seriesSlug, baseDuration),
    year: `${2022 + idx}`,
    description: `الموسم ${idx + 1} من المسلسل`,
  }));
}

