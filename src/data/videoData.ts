export function getVideosByCategory(category: VideoCategorySlug | null): VideoItem[] {
  if (!category) return VIDEOS;
  return VIDEOS.filter((v) => v.category === category);
}

export function getFeaturedVideos(): VideoItem[] {
  return VIDEOS.filter((v) => v.isFeatured);
}

export function formatViews(views: number): string {
  if (views >= 1000000) return (views / 1000000).toFixed(1) + 'م';
  if (views >= 1000) return (views / 1000).toFixed(1) + 'ك';
  return views.toString();
}
